#include "ft_ssl_md5.h"

uint32_t	leftrotate(uint32_t x, size_t c)
{
	return ((((x) << (c)) | ((x) >> (32 - (c)))));
}

uint32_t	rightrotate(uint32_t x, size_t c)
{
	return ((((x) >> (c)) | ((x) << (32 - (c)))));
}
